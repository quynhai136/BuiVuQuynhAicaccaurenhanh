a = float(input("Nhập số nguyên a: "))
b = float(input("Nhập số nguyên b: "))
c = float(input("Nhập số nguyên c: "))
d = float(input("Nhập số nguyên d: "))
nho_nhat = a 
if b < nho_nhat:
    nho_nhat = b
if c < nho_nhat:
    nho_nhat = c
if d < nho_nhat:
    nho_nhat = d
print("Số nhỏ nhất là:", nho_nhat)

